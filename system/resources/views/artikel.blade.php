					<p>Niagara adalah air terjun besar di Sungai Niagara yang berada di garis perbatasan internasional antara negara bagian Amerika Serikat New York dengan Provinsi Kanada Ontario. Air terjun ini berjarak sekitar 17 mil (27 km) sebelah utara barat laut dari Buffalo, New York dan 75 mil (120 km) tenggara Toronto, Ontario.

					Niagara adalah nama kelompok dari tiga air terjun. Ketiga air terjun tersebut adalah air terjun Horseshoe (kadang-kadang disebut sebagai air terjun Kanada), air terjun Amerika, dan yang lebih kecil yakni air terjun Bridal Veil yang dipisahkan oleh sebuah pulau bernama Luna Island dari air terjun utama.

					Meski tidak terlalu tinggi, Niagara merupakan air terjun yang sangat lebar dan terpopuler di dunia. Lebih dari 6 juta kaki kubik (168.000 m3) air per menit dijatuhkan dan ini merupakan air terjun yang paling kuat di Amerika Utara.

					Niagara juga terkenal akan pelangi indahnya yang melintang di tengah derasnya air terjun. Keindahan alam yang terdapat di sekeliling Niagara membuat jutaan orang dari setiap belahan dunia mengunjunginya setiap hari. Apalagi kalau bukan untuk melihat air terjun yang paling populer ini. Sehingga, devisa dan turis manca negara pun banyak mengalir bagi kedua negara ini.

					Selain keindahan itu, air terjun ini juga memisahkan antara 2 negara antara lain Kanada dan New York, Amerika Serikat. Turis-turis bisa mendapatkan sensasi air terjun ini dari kedua sisi tersebut.

					Pada 24 Oktober 1901, seorang guru sekolah asal Michigan berusia 63 tahun Annie Edson Taylor menjadi orang pertama yang pergi ke air terjun di dalam sebuah tong. Sepuluh tahun berselang, Bobby Leach pergi ke Horseshoe Falls dengan menggunakan tong baja pada tahun 1911.</p>

